    <p>The foreign exchange market works through financial institutions, and operates on several levels. Behind the
        scenes, banks turn to a smaller number of financial firms known as "dealers", who are involved in large
        quantities of foreign exchange trading. Most foreign exchange dealers are banks, so this behind-the-scenes
        market is sometimes called the interbank market (although a few insurance companies and other kinds of financial
        firms are involved). Trades between foreign exchange dealers can be very large, involving hundreds of millions
        of dollars. Because of the sovereignty issue when involving two currencies, Forex has little (if any)
        supervisory entity regulating its actions.</p><br>
