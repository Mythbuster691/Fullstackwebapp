    <p>Several large online travel sites are, in effect, travel agencies. These sites send the hotels' information and
        rates downstream to literally thousands of online travel sites, most of which act as travel agents. They can
        then receive commission payments from the hotels for any business booked on their websites.</p><br><br>
    <h1>Individual hotel websites</h1><br>
