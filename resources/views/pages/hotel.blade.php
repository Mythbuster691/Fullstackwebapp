    <p>Large hotel chains typically have direct connections to the airline national distribution
        systems(GDS,Sabre,Galileo,Amadeus and Worldspan). These in turn provide hotel information directly to the
        hundreds of thousands of travel agents that align themselves with one of these systems. Individual hotels and
